//datatypes in dart
void main() {
  print("The following are the common datatypes used in dart.");
  // Var
  var name = "samuel";
  print("Var:$name");

  // Const
  const String firstname = "John";
  print("Const:$firstname");

  // Final
  final String lastname = "Smith";
  print("Final:$lastname");

  // String
  String username = "onaya";
  print("String:$username");

  // Integer
  int age = 30;
  print("Int:$age");

  // Decimals
  double version = 23.65;
  print("Double:$version");

  // Dynamic
  dynamic guess = "pavillion";
  print("Dynamic:$guess");

  bool isMan = false;
  print("Bool:$isMan");

  // Declearing but not using
  var car;
  int model;
  car = "Lexus";
  print(car);
  model = 20;
  print(model);
}
